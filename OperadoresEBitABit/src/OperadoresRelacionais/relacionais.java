package OperadoresRelacionais;

public class relacionais {

	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		
		//igualdade
		if(a == b)
		{
			System.out.println("Correto");
		} else
		{
			System.out.println("Incorreto");
		}
		
		System.out.println();
		
		//diferente
		if(a != b)
		{
			System.out.println("Correto");
		} else
		{
			System.out.println("Incorreto");
		}
		
		System.out.println();
		
		//maior que
		if(a > b)
		{
			System.out.println("Correto");
		} else
		{
			System.out.println("Incorreto");
		}
		
		System.out.println();
		
		//menor que
		if(a < b)
		{
			System.out.println("Correto");
		} else
		{
			System.out.println("Incorreto");
		}
		
		System.out.println();
	}

}
