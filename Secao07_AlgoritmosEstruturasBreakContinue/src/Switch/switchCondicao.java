package Switch;

public class switchCondicao {

	public static void main(String[] args) {
		int x = 13;
		switch(x)
		{
			case 10:
				System.out.println("é igual a 10");
				break;
			case 12:
				System.out.println("é igual a 12");
				break;
			default:
				System.out.println("Nenhum dos valores");
		}
		
		System.out.println("FIM DA OPERAÇÃO");
	}

}
