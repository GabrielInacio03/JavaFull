
public class main {

	public static void main(String[] args) {
		//Escapes
		System.out.println("Carlos");
		System.out.println("Silva");
		System.out.println("-----------------------------");
		System.out.println("Carlos\n");
		System.out.println("Silva");
		System.out.println("-----------------------------");
		System.out.println("Carlos\t");
		System.out.println("Silva");
		System.out.println("-----------------------------");
		System.out.println("Carlos\\");
		System.out.println("Silva");
	}

}
